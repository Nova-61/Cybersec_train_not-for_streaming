import numpy as np

# 1. Загрузите или вставьте ваши данные сюда
# Массивы должны быть одномерными и одинаковой длины
# Например, если у вас есть текстовый файл, можно использовать np.loadtxt()
X = np.array([...]) # Ваши координаты X
Y = np.array([...]) # Ваши координаты Y
W = np.array([...]) # Ваши координаты W (высота)

# Для примера исключим пустые значения, если они есть
# Обязательно преобразуем в одномерные массивы (flatten)
X_f = X.flatten()
Y_f = Y.flatten()
W_f = W.flatten()

# 2. Формируем матрицу M и вектор b для МНК
M = np.vstack([X_f, Y_f, W_f, np.ones_like(X_f)]).T
b = X_f**2 + Y_f**2 + W_f**2

# 3. Решаем линейную систему методом наименьших квадратов
v, residues, rank, s = np.linalg.lstsq(M, b, rcond=None)
A, B, C, D = v

# 4. Пересчитываем в координаты центра и радиус
X_0 = A / 2
Y_0 = B / 2
W_0 = C / 2
R = np.sqrt(D + X_0**2 + Y_0**2 + W_0**2)

print("--- РЕЗУЛЬТАТЫ РАСЧЕТА МНК ---")
print(f"Координаты центра сферы: X0 = {X_0:.4f}, Y0 = {Y_0:.4f}, W0 = {W_0:.4f}")
print(f"Радиус кривизны поверхности (R): {R:.2f} мм")